//using classList - we can see how many classes are there in an element
//obj.classList

//classList.add() to add new classes
//classList.remove() to remove classes
//classList.contains() to check if class exists 
//classList.toggle() to toggle between add & remove



let heading = document.querySelector('h1');
heading.classList;

// add any class

heading.classList.add("abc")

heading.classList.contains("xyz") // if exist then true , if not then false

heading.classList.toggle("abc") // REMOVE THIS CLASS , if abc is there then remove
heading.classList.toggle("abc") // ADD THIS CLASS , if abc is off then will turn on








